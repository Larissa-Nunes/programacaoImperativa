// Operadores

function soma(a, b){
    return a + b
}

console.log(soma(2, 3));

function subtração(a, b){
    return a - b
}

console.log(subtração(10, 2));

function multiplicação(a, b){
    return a * b
}

console.log(multiplicação(7, 6));

function divisão(a, b){
    return a / b
}

console.log(divisão(25, 5));