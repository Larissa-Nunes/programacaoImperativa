// Revisão de Variáveis
// typeof = verifica o tipo de variável
// exemplo: string, number, boolean

var nome = "Larissa"; // string (texto)
var idade = 25; // número inteiro
var peso = 62.5; // número com flutuante ou casas decimais
var temFaculdade = true; // lógico o booleano (verdadeiro ou falso)
var ehFeia = false;

console.log(typeof nome);
console.log(typeof idade);
console.log(typeof peso);
console.log(typeof temFaculdade);
console.log(typeof ehFeia);